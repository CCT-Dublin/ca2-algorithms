package enums;
/**
 * Defines the different types of admin roles available in the Sugar Blossom
 * Department Store. Each employee is assigned one specific manager type.
 *
 * This enum helps maintain consistency in employee records and prevents
 * invalid role descriptions from being entered manually.
 *
 * @author mariana
 */
public enum ManagerType {
    BEAUTY_CONSULTANT,
    FLOOR_SUPERVISOR,
    ASSISTANT_MANAGER,
    STORE_MANAGER,
    SENIOR_MANAGER,
    GENERAL_MANAGER
}